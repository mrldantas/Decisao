# Faça um Programa que peça dois números e imprima o maior deles.

primeiroNumero = int(input("Insira o primeiro numero: "))
segundoNumero = int(input("Insira o segundo numero: "))

if (primeiroNumero > segundoNumero):
    print(f"O número {primeiroNumero} é maior que o {segundoNumero}")
else:
    print(f"O número {segundoNumero} é maior que o {primeiroNumero}")
